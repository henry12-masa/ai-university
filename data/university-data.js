function detectKind(key, title){
  const s = key + title;
  if(/english|英語|sils/.test(s)) return "english";
  if(/math|数学/.test(s)) return "math";
  if(/japanese|国語|現代文|古文|漢文|letters|文学|culture/.test(s)) return "japanese";
  if(/history|geography|ethics|politics|society|law|economics|business|social|日本史|世界史|地理|倫理|政経|現代社会|法|経済|商|政治/.test(s)) return "social";
  if(/physics|chemistry|biology|earth|science|medicine|pharmacy|nursing|物理|化学|生物|地学|理工|医|薬|看護/.test(s)) return "science";
  if(/info|情報/.test(s)) return "info";
  return "general";
}

function makeQuestions(key, title, group){
  const kind = detectKind(key, title);
  const fields = fieldPools[kind] || fieldPools.general;
  const levels = ["基礎", "標準", "応用", "難関", "実戦"];
  const questions = [];
  for(let i=1;i<=200;i++){
    const field = fields[(i-1)%fields.length];
    const level = levels[(i-1)%levels.length];
    const correctIndex = (i-1)%4;
    const choices = [
      `${field}で最初に根拠を確認する`,
      `選択肢だけを見て感覚で選ぶ`,
      `本文や条件を読まずに公式だけ使う`,
      `設問番号だけで答えを推測する`
    ];
    const answer = choices[correctIndex];
    questions.push({
      id: `${key}-${String(i).padStart(3,"0")}`,
      no: i,
      field,
      level,
      question: `${title} 対策 第${i}問：${field}の問題で得点を安定させるために、最も適切な解き方はどれか。`,
      choices: shuffleChoices(choices, i),
      answer,
      explanation: `AI解説：${title}では、${field}の根拠を本文・条件・資料から確認する姿勢が重要です。感覚で選ばず、設問要求を先に押さえると正答率が上がります。`
    });
  }
  return questions;
}

function shuffleChoices(arr, seed){
  const a = arr.slice();
  for(let i=a.length-1;i>0;i--){
    const j = (seed + i * 7) % (i+1);
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

